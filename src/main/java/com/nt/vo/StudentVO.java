package com.nt.vo;

public class StudentVO {

	private String rollNumber, studentName, mathsMarks, physicsMarks, chemistryMarks;

	public String getMathsMarks() {
		return mathsMarks;
	}

	public void setMathsMarks(String mathsMarks) {
		this.mathsMarks = mathsMarks;
	}

	public String getChemistryMarks() {
		return chemistryMarks;
	}

	public void setChemistryMarks(String chemistryMarks) {
		this.chemistryMarks = chemistryMarks;
	}

	public String getStudentName() {
		return studentName;
	}

	public void setStudentName(String studentName) {
		this.studentName = studentName;
	}

	public String getPhysicsMarks() {
		return physicsMarks;
	}

	public void setPhysicsMarks(String physicsMarks) {
		this.physicsMarks = physicsMarks;
	}

	public String getRollNumber() {
		return rollNumber;
	}

	public void setRollNumber(String rollNumber) {
		this.rollNumber = rollNumber;
	}

}
